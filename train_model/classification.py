def create_smote_pipeline(base_model, need_scale=False):

    steps = [
        SimpleImputer(strategy='median'),
        SMOTE(sampling_strategy='auto', random_state=42) # 自動平衡少數派
    ]
    if need_scale:
        steps.append(StandardScaler())
    steps.append(base_model)


    return make_imb_pipeline(*steps)

