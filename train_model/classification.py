def extract_bert_in_batches(text_list, max_len, desc_text):
    embeddings = []
    for i in tqdm(range(0, len(text_list), 16), desc=desc_text):
        batch_texts = text_list[i:i+16]
        inputs = tokenizer(batch_texts, padding=True, truncation=True, max_length=max_len, return_tensors="pt")
        inputs = {k: v.to(device) for k, v in inputs.items()}
        with torch.no_grad():
            outputs = model(**inputs)
            cls_vectors = outputs.last_hidden_state[:, 0, :].cpu().numpy()
            embeddings.append(cls_vectors)
    return np.vstack(embeddings)

